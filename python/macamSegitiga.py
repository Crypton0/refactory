## segitiga siku-siku menghadap ke kanan

for i in range(0,10):
	for j in range(0,i):
		print('*',end='')
	print('\n',end='')

mulai = 0
while mulai < 11:
	awal = 9
	while awal >= mulai:
		print('#', end='')
		awal -= 1
	print('\n',end='')
	mulai += 1

print('\n\n')














